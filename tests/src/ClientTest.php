namespace Updivision\Xmpp;

/**
 * Generated by PHPUnit_SkeletonGenerator 1.2.1 on 2014-01-17 at 10:05:30.
 *
 * @coversDefaultClass Updivision\Xmpp\Client
 */
class ClientTest extends \PHPUnit_Framework_TestCase
{
    /**
     * @var Client
     */
    protected $object;

    /**
     * @var \PHPUnit_Framework_MockObject_MockObject
     */
    protected $options;

    /**
     * @var \PHPUnit_Framework_MockObject_MockObject
     */
    protected $connection;

    /**
     * @var \PHPUnit_Framework_MockObject_MockObject
     */
    protected $eventManager;

    /**
     * @var \PHPUnit_Framework_MockObject_MockObject
     */
    protected $implementation;

    /**
     * Sets up the fixture, for example, opens a network connection.
     * This method is called before a test is executed.
     *
     * @return void
     */
    protected function setUp()
    {
        $this->options = $this->getMockBuilder('Updivision\Xmpp\Options')
            ->disableOriginalConstructor()
            ->getMock();

        $this->connection = $this->getMock('Updivision\Xmpp\Connection\ConnectionInterface');

        $this->options->expects($this->any())
            ->method('getConnection')
            ->willReturn($this->connection);

        $this->eventManager = $this->getMock('Updivision\Xmpp\Event\EventManagerInterface');
        $this->implementation = $this->getMock('Updivision\Xmpp\Protocol\ImplementationInterface');

        $this->options->expects($this->any())
            ->method('getImplementation')
            ->willReturn($this->implementation);

        $this->object  = new Client($this->options, $this->eventManager);
    }

    /**
     * Test constructor.
     *
     * @covers ::__construct
     * @covers ::setupImplementation
     * @uses Updivision\Xmpp\Client::getEventManager
     * @uses Updivision\Xmpp\Client::setEventManager
     * @uses Updivision\Xmpp\Client::getOptions
     */
    public function testConstructor()
    {
        $this->connection->expects($this->once())
            ->method('setEventManager')
            ->with($this->equalTo($this->eventManager));

        $this->connection->expects($this->once())
            ->method('setOptions')
            ->with($this->equalTo($this->options));

        $this->implementation->expects($this->once())
            ->method('registerListener')
            ->with($this->isInstanceOf('\Updivision\Xmpp\EventListener\Logger'));

        $this->implementation->expects($this->once())
            ->method('setEventManager')
            ->with($this->equalTo($this->eventManager));

        $this->implementation->expects($this->once())
            ->method('setOptions')
            ->with($this->equalTo($this->options));

        $this->implementation->expects($this->once())
            ->method('register');

        $object = new Client($this->options, $this->eventManager);
        $this->assertSame($this->eventManager, $object->getEventManager());
        $this->assertSame($this->options, $object->getOptions());
    }

    /**
     * @covers ::__construct
     * @covers ::getConnection
     * @uses Updivision\Xmpp\Client::setupImplementation
     * @uses Updivision\Xmpp\Client::getEventManager
     * @uses Updivision\Xmpp\Stream\SocketClient::__construct
     * @uses Updivision\Xmpp\Connection\AbstractConnection::setEventManager
     * @uses Updivision\Xmpp\Connection\AbstractConnection::setOptions
     * @uses Updivision\Xmpp\Connection\Socket::__construct
     * @uses Updivision\Xmpp\Connection\Socket::factory
     * @uses Updivision\Xmpp\Connection\Socket::setSocket
     * @uses Updivision\Xmpp\Event\EventManager::__construct
     */
    public function testConstructorCreatingDefaults()
    {
        $options = $this->getMockBuilder('Updivision\Xmpp\Options')
            ->disableOriginalConstructor()
            ->getMock();

        $options->expects($this->any())
            ->method('getImplementation')
            ->willReturn($this->implementation);

        $object = new Client($options);
        $this->assertInstanceOf('Updivision\Xmpp\Event\EventManagerInterface', $object->getEventManager());
        $this->assertInstanceOf('Updivision\Xmpp\Connection\ConnectionInterface', $object->getConnection());
    }

    /**
     * Test connection.
     *
     * @covers ::connect
     * @uses Updivision\Xmpp\Client::__construct
     * @uses Updivision\Xmpp\Client::setupImplementation
     */
    public function testConnect()
    {
        $this->connection->expects($this->once())
            ->method('connect');

        $this->object->connect();
    }

    /**
     * Test disconnecting.
     *
     * @covers ::disconnect
     * @uses Updivision\Xmpp\Client::__construct
     * @uses Updivision\Xmpp\Client::setUpImplementation
     */
    public function testDisconnect()
    {
        $this->connection->expects($this->once())
            ->method('disconnect');

        $this->object->disconnect();
    }

    /**
     * Test sending data.
     *
     * @covers ::send
     * @uses Updivision\Xmpp\Client::__construct
     * @uses Updivision\Xmpp\Client::setUpImplementation
     */
    public function testSend()
    {
        $this->connection->expects($this->once())
            ->method('send')
            ->with($this->equalTo('test'));

        $message = $this->getMock('Updivision\Xmpp\Protocol\ProtocolImplementationInterface');
        $message->expects($this->once())
            ->method('toString')
            ->willReturn('test');

        $this->object->send($message);
    }

    /**
     * Test setting and getting event manager.
     *
     * @covers ::getEventManager
     * @covers ::setEventManager
     * @uses Updivision\Xmpp\Client::__construct
     * @uses Updivision\Xmpp\Client::setUpImplementation
     */
    public function testSetAndGetEventManager()
    {
        $eventManager = $this->getMock('Updivision\Xmpp\Event\EventManagerInterface');
        $this->assertSame($eventManager, $this->object->setEventManager($eventManager)->getEventManager());
    }

    /**
     * Test getting options object.
     *
     * @covers ::getOptions
     * @uses Updivision\Xmpp\Client::__construct
     * @uses Updivision\Xmpp\Client::setUpImplementation
     */
    public function testGetOptions()
    {
        $this->assertSame($this->options, $this->object->getOptions());
    }

    /**
     * Test setter Option Connection in Client constructor
     *
     * @covers ::__construct
     * @uses Updivision\Xmpp\Client::__construct
     * @uses Updivision\Xmpp\Client::getOptions
     * @uses Updivision\Xmpp\Client::setUpImplementation
     * @uses Updivision\Xmpp\Options::getConnection
     */
    public function testOptionsConnection()
    {
        $options = new Options();
        $client = $this->getMock('\Updivision\Xmpp\Client', array('setupImplement'), array($options));

        $optionsAssert = $client->getOptions();
        $this->assertInstanceOf('Updivision\Xmpp\Connection\ConnectionInterface', $optionsAssert->getConnection());
    }
}
